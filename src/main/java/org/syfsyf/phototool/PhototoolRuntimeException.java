package org.syfsyf.phototool;

public class PhototoolRuntimeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public PhototoolRuntimeException() {
		super();

	}

	public PhototoolRuntimeException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public PhototoolRuntimeException(String message, Throwable cause) {
		super(message, cause);

	}

	public PhototoolRuntimeException(String message) {
		super(message);

	}

	public PhototoolRuntimeException(Throwable cause) {
		super(cause);

	}

}
